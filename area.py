#to get the area of the shapes- square, rectangle, circle and triangle

#to get the area of square
a=int(input("enter the edge length"))
area=a*a
print(area)

#to get the area of rectangle
l=int(input("enter the edge length"))
b=int(input("enter the breadth"))
area=l*b
print(area)

#to get the area of circle
pi=3.14
r=int(input("enter the radius"))
area=pi*r*r
print(area)

#to get the area of triangle
b=int(input("enter the breadth"))
h=int(input("enter the height"))
area=0.5*b*h
print(area)
